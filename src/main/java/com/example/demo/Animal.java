package com.example.demo;

public class Animal {
    String ownerName;
    String petName;
    String breed;
    String typeOfAnimal;
    String gender;
    double weight;

    public Animal(String ownerName, String petName, String breed, String typeOfAnimal, String gender, double weight) {
        this.ownerName = ownerName;
        this.petName = petName;
        this.breed = breed;
        this.typeOfAnimal = typeOfAnimal;
        this.gender = gender;
        this.weight = weight;
    }
}
